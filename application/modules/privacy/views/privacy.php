        <h1>Ministry of Health Uganda HRM Attend - Data Privacy Policy</h1>

    <h2>Introduction:</h2>
    <p>The Ministry of Health Uganda is committed to ensuring the privacy and security of employee data in the Attendance Tracking System for the health sector. This policy outlines the principles and practices governing the collection, use, and protection of personal information.</p>

    <h2>1. Data Collection and Purpose:</h2>
    <p>We collect and process employee information solely for attendance tracking and related administrative purposes. The data includes employee names, face and fingrprint biometrics, ihris identification number, clockin records, health facility of deployment. The primary goal is to facilitate enrollment efficient workforce management within the health sector. Apart from the biometrics, the rest of the data is picked from iHRIS - integrated Human Resource Information System which is used by Ministry to manage health workers.</p>

    <h2>2. Consent:</h2>
    <p>By participating in the Attendance Tracking System, employees implicitly consent to the collection and use of their data for the specified purposes. Personal information and clockin data will not be shared or used for any other purposes without explicit consent.</p>

    <h2>3. Data Security:</h2>
    <p>We prioritize the security of employee data. Robust technical and organizational measures are in place to prevent unauthorized access, disclosure, alteration, or destruction of personal information. Access to data is restricted to authorized personnel only or after verification.</p>

    <h2>4. Data Retention:</h2>
    <p>Health Workers attendance and biometrics -data will be retained for the duration necessary to fulfill the purposes outlined in this policy.</p>

    <h2>5. Data Sharing:</h2>
    <p>We do not sell, trade, or otherwise transfer employee data to external parties. Information may be shared within the Ministry of Health on a need-to-know basis for administrative purposes only.  Attendance data will be shared for decision making with the offices as aggregates at facility, regional, and national level. Sharing of individual records will require the authorisation of the Commissioner HRM.</p>

    <h2>6. Third-Party Services:</h2>
    <p>In some cases, third-party service providers may be engaged to support the Attendance Tracking System. These providers are carefully vetted to ensure they adhere to the same stringent data privacy standards outlined in this policy and the MoH ICT Guildelines <?php date('Y')?></p>

    <h2>7. Individual Rights:</h2>
    <p>Employees have the right to access, correct, and update their personal information from the HR Office at MoH or their districts. Requests for information or modifications should be directed to the designated Human Resource department.</p>

    <h2>8. Data Breach Notification:</h2>
    <p>In the event of a data breach that compromises the security of employee information, affected individuals and relevant authorities will be promptly notified in accordance with legal obligations.</p>

    <h2>9. Policy Review:</h2>
    <p>This policy is subject to periodic review to ensure compliance with evolving data protection regulations and industry best practices. Any updates will be communicated to employees.</p>

    <h2>Contact Information:</h2>
    <p>For inquiries or concerns regarding data privacy, please contact the Ministry of Health Uganda's on <a href="mailto:hmissupport.health.go.ug">hmissupport.health.go.ug</a>.</p>

